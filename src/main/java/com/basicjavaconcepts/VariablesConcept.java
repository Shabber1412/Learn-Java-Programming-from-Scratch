package com.basicjavaconcepts;

public class VariablesConcept {

    public static void main(String[] args) 
    {        
        String name = "Shabber Hussain"; // Instance variable        
        final String country = "India"; // Constant variable
        int age = 28; // Instance variable
        double salary = 75000.50; // Instance variable
        boolean isEmployed = true; // Instance variable
        char grade = 'A'; // Instance variable
        float height = 5.9f; // Instance variable
    
        // Printing variable values
        System.out.println("Name: " + name);
        System.out.println("Country: " + country);
        System.out.println("Age: " + age);
        System.out.println("Salary: " + salary);
        System.out.println("Is Employed: " + isEmployed);
        System.out.println("Grade: " + grade);
        System.out.println("Height: " + height);
    }
}

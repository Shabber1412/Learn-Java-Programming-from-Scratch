package com.basicjavaconcepts;

public class Main {
    public static void main(String[] args) {
        // Printing "Hello world!" to the console
        System.out.println("Hello world!");
        
        // Printing "Hello world!" twice on separate lines
        System.out.println("Hello world!\nHello world!");

        // Printing a pattern
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        System.out.println("*****");
        System.out.println("******");
    }
}